void POVDisplay::Init()
{
	SIM->SCGC6 |= (SIM_SCGC6_PIT_MASK);
	PIT->MCR = (PIT->MCR & ~PIT_MCR_MDIS_MASK);
	PIT->CHANNEL[0].LDVAL = 1398;// 14MHZ clock frequency
	PIT->CHANNEL[0].TCTRL = (PIT->CHANNEL[0].TCTRL & ~PIT_TCTRL_TEN_MASK) | PIT_TCTRL_TEN_MASK;
	PIT->CHANNEL[0].TCTRL = (PIT->CHANNEL[0].TCTRL & ~PIT_TCTRL_TIE_MASK) | PIT_TCTRL_TIE_MASK;
	NVIC_EnableIRQ(PIT_IRQn);

}

void POVDisplay::setDisplayTemp(int Temperature)
{
	int temp[3];
	temp[2] = (Temperature%10) ;// ones digit for Temperature
	Temperature = Temperature/10;
	temp[1] = (Temperature%10) ; // tens digit for Temperature
	Temperature = Temperature/10;
	temp[0] = (Temperature%10) ;// hundreds digit for Temperature
	bookmark = 0;
	if (temp[0]== 0) // if statment when hundreds digit is 0 is not needed to be displaied
	{
		for(int i = 1; i < 3; i++) // tens and ones are put in buffer
		{
			for(int j = fontIndexPOV[temp[i]]; j < (fontIndexPOV[temp[i]+1]); j++) // loop that calls index to find the frist and last part of the pattern for the digit
			{
				g_displaybuff[bookmark] = font[j]; // puts the current pattern into a buffer
				bookmark++; // bookmark to track how long lenght is
			}
			for(int j = 0; j < 4; j++) // use to added space between numbers
			{
				g_displaybuff[bookmark] = 0x0;
				bookmark++;
			}
		}
	}
	else // when hunders is not 0 this loop is called
	{
		for(int i = 0; i < 3; i++)// loop from 3 digit
		{
			for(int j = fontIndexPOV[temp[i]]; j < (fontIndexPOV[temp[i]+1]); j++) // loop that calls index to find the frist and last part of the pattern for the digit 
			{
				g_displaybuff[bookmark] = font[j]; // addes pattern into buffer
				bookmark++;
			}
			for(int j = 0; j < 4; j++)
			{
				g_displaybuff[bookmark] = 0x0; // use to added space between numbers 
				bookmark++;
			}
		}
	}
	
	for(int j = fontIndexPOV[14]; j < (fontIndexPOV[15]); j++)
	{
		g_displaybuff[bookmark] = font[j];
		bookmark++;
	}
	for(int j = 0; j < 4; j++)
		{
			g_displaybuff[bookmark] = 0x0;
			bookmark++;
		}
	for(int j = fontIndexPOV[12]; j < (fontIndexPOV[13]); j++)
	{
		g_displaybuff[bookmark] = font[j];
		bookmark++;
	}
	for(int j = 0; j < 20; j++)
		{
			g_displaybuff[bookmark] = 0x0;
			bookmark++;
		}
	displaycounter = bookmark;

} // end setDisplayTemp

void POVDisplay::setDisplayTime(int Time)
{
	
	bookmark = 0;
	int tempbuff[4];
	tempbuff[3] = (Time%10) ;// ones digit for minutes
	Time = Time/10;
	tempbuff[2] = (Time%10) ; // tens digit for miunrs
	Time = Time/10;
	tempbuff[1] = (Time%10) ;// ones digit for hours
	Time = Time/10;
	tempbuff[0] = (Time%10) ;// tens digit for hours
	
	for(int i = 0; i < 2; i++)// loop from 3 digit
		{
			for(int j = fontIndexPOV[tempbuff[i]]; j < (fontIndexPOV[tempbuff[i]+1]); j++) // loop that calls index to find the frist and last part of the pattern for the digit 
			{
				g_displaybuff[bookmark] = font[j]; // addes pattern into buffer
				bookmark++;
			}
			for(int j = 0; j < 5; j++)
			{
				g_displaybuff[bookmark] = 0x0; // use to added space between numbers 
				bookmark++;
			}
		}
		for(int j = fontIndexPOV[10]; j < (fontIndexPOV[11]); j++) // loop that calls index to find the frist and last part of the pattern for the digit 
		{
			g_displaybuff[bookmark] = font[j]; // addes pattern into buffer
			bookmark++;
		}
		for(int j = 0; j < 5; j++)
		{
			g_displaybuff[bookmark] = 0x0; // use to added space between numbers 
			bookmark++;
		}
		for(int i = 2; i < 4; i++)// loop from 3 digit
		{
			for(int j = fontIndexPOV[tempbuff[i]]; j < (fontIndexPOV[tempbuff[i]+1]); j++) // loop that calls index to find the frist and last part of the pattern for the digit 
			{
				g_displaybuff[bookmark] = font[j]; // addes pattern into buffer
				bookmark++;
			}
			for(int j = 0; j < 10; j++)
			{
				g_displaybuff[bookmark] = 0x0; // use to added space between numbers 
				bookmark++;
			}
		}
		for(int j = 0; j < 20; j++)
		{
			g_displaybuff[bookmark] = 0x0;
			bookmark++;
		}
	displaycounter = bookmark;
}// end setDisplayTime
void POVDisplay::Display()
{
	g_DLED.UpdateLED(g_displaybuff[displaycounter]);
	
	if (displaycounter == bookmark)
	{
		displaycounter = 0;
	}
	displaycounter++;
}

